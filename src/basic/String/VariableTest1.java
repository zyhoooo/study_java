/*
数据类型：
8种基本数据类型
byte,short,int,long
float,double
char
boolean
引用数据类型
类class,接口interface,数组[]
 */
public class VariableTest1 {
    public static void main(String[] args) {
        byte b1 = 12;
        short s1 = 128;
        int i1 = 1234;
        long l1 = 12345l;
        System.out.println(l1);

        //浮点型
        float f1 = 1.23f;
        double d1 = 1.2345;
        System.out.println(f1);

        //字符型
        char c1 = 'a';
        System.out.println(c1);

        //布尔
        boolean bo1 = false;
        System.out.println(bo1);
    }
}
